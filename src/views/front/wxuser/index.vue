<script>
import {
  listWxuser,
  getWxuser,
  delWxuser,
  addWxuser,
  updateWxuser,
} from "@/api/front/wxuser";

export default {
  name: "Wxuser",
  //获取字典类型数据
  dicts: ["sys_user_sex", "front_user_status"],
  data() {
    return {
      // 遮罩层
      loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 微信用户表格数据
      wxuserList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        openId: null,
        nickname: null,
        name: null,
        sex: null,
        province: null,
        city: null,
        district: null,
        photo: null,
        phone: null,
        email: null,
        isDisable: null,
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        openId: [
          {
            required: true,
            message: "微信用户openid不能为空",
            trigger: "blur",
          },
        ],
      },
    };
  },
  created() {
    this.getList();
  },
  methods: {
    /** 查询微信用户列表 */
    getList() {
      this.loading = true;
      listWxuser(this.queryParams).then((response) => {
        this.wxuserList = response.rows;
        this.total = response.total;
        this.loading = false;
      });
    },
    //格式化table中的内容
    formatterDisable(row) {
      return row.isDisable === "0" ? "正常" : "禁用";
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        id: null,
        openId: null,
        nickname: null,
        name: null,
        sex: null,
        province: null,
        city: null,
        district: null,
        photo: null,
        phone: null,
        email: null,
        isDisable: null,
        createTime: null,
        updateTime: null,
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map((item) => item.id);
      this.single = selection.length !== 1;
      this.multiple = !selection.length;
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.open = true;
      this.title = "添加微信用户";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const id = row.id || this.ids;
      getWxuser(id).then((response) => {
        this.form = response.data;
        this.open = true;
        this.title = "修改微信用户";
      });
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate((valid) => {
        if (valid) {
          if (this.form.id != null) {
            updateWxuser(this.form).then((response) => {
              this.$modal.msgSuccess("修改成功");
              this.open = false;
              this.getList();
            });
          } else {
            addWxuser(this.form).then((response) => {
              this.$modal.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const ids = row.id || this.ids;
      this.$modal
        .confirm('是否确认删除微信用户编号为"' + ids + '"的数据项？')
        .then(function () {
          return delWxuser(ids);
        })
        .then(() => {
          this.getList();
          this.$modal.msgSuccess("删除成功");
        })
        .catch(() => {});
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download(
        "front/wxuser/export",
        {
          ...this.queryParams,
        },
        `wxuser_${new Date().getTime()}.xlsx`
      );
    },
  },
};
</script>
